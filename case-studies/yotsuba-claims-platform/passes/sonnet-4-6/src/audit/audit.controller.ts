// =============================================================================
// Yotsuba Insurance Holdings — Claims Processing Platform
// src/audit/audit.controller.ts
//
// Audit controller. Exposes:
//   GET /audit — paginated, filtered audit log (auditor-only)
//
// DESIGN INVARIANT (ADR-002): This controller is READ-ONLY. There is no
// write, update, or delete pathway exposed here. The audit log is strictly
// append-only; all writes originate from AuditService.record() called
// by the audit interceptor or service layer.
//
// Access is restricted to the `auditor` role via the RolesGuard.
// =============================================================================

import {
  Controller,
  Get,
  HttpStatus,
  Query,
  UseGuards,
} from '@nestjs/common';
import {
  ApiBearerAuth,
  ApiOperation,
  ApiQuery,
  ApiResponse,
  ApiTags,
} from '@nestjs/swagger';
import { UserRole } from '@prisma/client';
import { JwtAuthGuard } from '../common/jwt-auth.guard';
import { RolesGuard } from '../common/roles.guard';
import { Roles } from '../common/roles.decorator';
import { AuditService, AuditLogPage, AuditQueryFilters } from './audit.service';

// ---------------------------------------------------------------------------
// Controller
// ---------------------------------------------------------------------------

@ApiTags('audit')
@ApiBearerAuth()
@UseGuards(JwtAuthGuard, RolesGuard)
@Controller('audit')
export class AuditController {
  constructor(private readonly auditService: AuditService) {}

  // -------------------------------------------------------------------------
  // GET /audit
  // -------------------------------------------------------------------------

  /**
   * Returns a paginated, filtered view of the immutable audit log.
   *
   * Only users with the `auditor` role may call this endpoint. All query
   * parameters are optional; omitting them returns the most-recent 100
   * events across all claims and actors.
   *
   * Filter parameters are ANDed together:
   *   - `from` / `to`   — ISO-8601 datetime bounds on `ts`
   *   - `actor`         — filter by actor_id (exact match)
   *   - `claim_id`      — filter by claim_id (exact match)
   *   - `action`        — prefix match, e.g. "claim." returns all claim events
   *   - `limit`         — page size, default 100, max 500
   *   - `offset`        — pagination offset, default 0
   */
  @Get()
  @Roles(UserRole.auditor)
  @ApiOperation({
    summary: 'Query the immutable audit log.',
    description:
      'Returns a paginated, filtered view of all audit events. ' +
      'Restricted to the `auditor` role. ' +
      'All filter parameters are optional and are ANDed together. ' +
      'Results are ordered by timestamp descending (most-recent first). ' +
      'This endpoint is read-only — the audit log has no write, update, or ' +
      'delete pathway per ADR-002.',
  })
  @ApiQuery({
    name: 'from',
    required: false,
    type: String,
    description: 'ISO-8601 datetime lower bound on audit event timestamp (inclusive).',
    example: '2024-01-01T00:00:00.000Z',
  })
  @ApiQuery({
    name: 'to',
    required: false,
    type: String,
    description: 'ISO-8601 datetime upper bound on audit event timestamp (inclusive).',
    example: '2024-12-31T23:59:59.999Z',
  })
  @ApiQuery({
    name: 'actor',
    required: false,
    type: String,
    description: 'Filter by actor_id (exact match). Returns only events generated by this user.',
    example: 'clxxxxxxxxxxxxxxxxxxxxx',
  })
  @ApiQuery({
    name: 'claim_id',
    required: false,
    type: String,
    description: 'Filter by claim_id (exact match). Returns all audit events for a specific claim.',
    example: 'clyyyyyyyyyyyyyyyyyyyyy',
  })
  @ApiQuery({
    name: 'action',
    required: false,
    type: String,
    description:
      'Prefix match on the action field. ' +
      'E.g. "claim." returns all claim-related events; ' +
      '"reserve." returns all reserve events; ' +
      '"claim.created" returns only claim creation events.',
    example: 'claim.',
  })
  @ApiQuery({
    name: 'limit',
    required: false,
    type: Number,
    description: 'Page size. Default 100, maximum 500.',
    example: 100,
  })
  @ApiQuery({
    name: 'offset',
    required: false,
    type: Number,
    description: 'Pagination offset (zero-based). Default 0.',
    example: 0,
  })
  @ApiResponse({
    status: HttpStatus.OK,
    description: 'Paginated audit log page returned.',
    schema: {
      type: 'object',
      properties: {
        data: {
          type: 'array',
          items: {
            type: 'object',
            properties: {
              id:             { type: 'string', example: 'clxxxxxxxxxxxxxxxxxxxxx' },
              actor_id:       { type: 'string', example: 'clyyyyyyyyyyyyyyyyyyyyy' },
              actor_role:     { type: 'string', example: 'adjuster' },
              action:         { type: 'string', example: 'claim.created' },
              claim_id:       { type: 'string', nullable: true, example: 'clzzzzzzzzzzzzzzzzzzzzz' },
              target_id:      { type: 'string', nullable: true, example: null },
              payload_hash:   { type: 'string', example: 'a3f1e2b4c5d6...' },
              request_id:     { type: 'string', example: 'req-uuid-here' },
              correlation_id: { type: 'string', example: 'corr-uuid-here' },
              ts:             { type: 'string', format: 'date-time', example: '2024-06-15T10:30:00.000Z' },
            },
          },
        },
        total:  { type: 'number', example: 1542 },
        limit:  { type: 'number', example: 100 },
        offset: { type: 'number', example: 0 },
      },
    },
  })
  @ApiResponse({
    status: HttpStatus.UNAUTHORIZED,
    description: 'Missing or invalid Bearer token.',
  })
  @ApiResponse({
    status: HttpStatus.FORBIDDEN,
    description: 'Caller does not have the `auditor` role.',
  })
  @ApiResponse({
    status: HttpStatus.BAD_REQUEST,
    description: 'Invalid query parameter format (e.g. non-ISO date string).',
  })
  async getAuditLog(
    @Query('from')     from?: string,
    @Query('to')       to?: string,
    @Query('actor')    actor?: string,
    @Query('claim_id') claim_id?: string,
    @Query('action')   action?: string,
    @Query('limit')    limitRaw?: string,
    @Query('offset')   offsetRaw?: string,
  ): Promise<AuditLogPage> {
    const filters: AuditQueryFilters = {};

    if (from)     filters.from     = from;
    if (to)       filters.to       = to;
    if (actor)    filters.actor_id = actor;
    if (claim_id) filters.claim_id = claim_id;
    if (action)   filters.action   = action;

    // Parse numeric pagination params; fall back to service defaults on NaN.
    if (limitRaw !== undefined) {
      const parsed = parseInt(limitRaw, 10);
      if (!isNaN(parsed) && parsed > 0) {
        filters.limit = parsed;
      }
    }

    if (offsetRaw !== undefined) {
      const parsed = parseInt(offsetRaw, 10);
      if (!isNaN(parsed) && parsed >= 0) {
        filters.offset = parsed;
      }
    }

    return this.auditService.queryAuditLog(filters);
  }
}