// ─── Custom Jest matchers ─────────────────────────────────────────────────────

/**
 * .toBeValidCuid()
 * Asserts that the received string is a valid CUID (as generated by Prisma
 * @default(cuid())).
 *
 * @example
 * expect(response.body.id).toBeValidCuid();
 */
expect.extend({
  toBeValidCuid(received) {
    // CUIDs start with 'c' and are 25 characters long (cuid v1 / Prisma default)
    const pass =
      typeof received === 'string' &&
      /^c[a-z0-9]{24}$/.test(received);
    return {
      pass,
      message: () =>
        pass
          ? `expected ${JSON.stringify(received)} NOT to be a valid CUID`
          : `expected ${JSON.stringify(received)} to be a valid CUID (starts with 'c', 25 chars, lowercase alphanumeric)`,
    };
  },
});

/**
 * .toBeYen(expected)
 * Asserts that a reserve amount expressed as a string (Prisma Decimal → string
 * in JSON) equals the expected number when parsed.
 *
 * @example
 * expect(response.body.proposed_yen).toBeYen(1500000);
 */
expect.extend({
  toBeYen(received, expected) {
    const parsed = typeof received === 'string' ? Number(received) : received;
    const pass = Number.isFinite(parsed) && parsed === Number(expected);
    return {
      pass,
      message: () =>
        pass
          ? `expected ¥${parsed} NOT to equal ¥${expected}`
          : `expected ¥${parsed} to equal ¥${expected}`,
    };
  },
});

/**
 * .toHaveAuditAction(action)
 * Convenience matcher for checking that an audit event array contains a
 * specific action string.
 *
 * @example
 * expect(auditEvents).toHaveAuditAction('claim.created');
 */
expect.extend({
  toHaveAuditAction(received, action) {
    const isArray = Array.isArray(received);
    const pass =
      isArray && received.some((e) => e && e.action === action);
    return {
      pass,
      message: () =>
        pass
          ? `expected audit events NOT to contain action '${action}'`
          : `expected audit events to contain action '${action}', got: ${JSON.stringify(
              isArray ? received.map((e) => e?.action) : received,
            )}`,
    };
  },
});

