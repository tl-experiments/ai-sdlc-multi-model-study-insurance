// ─────────────────────────────────────────────────────────────────────────
// Yotsuba Insurance Holdings · Claims Processing Platform
// APPI service — the aggregator behind the two APPI-compliance
// hooks documented in brief.md (§Non-functional requirements):
//
//   * data-subject export (APPI Article 28 disclosure right):
//     return every piece of PII the platform holds about an
//     identified individual, in a single JSON document, across
//     every claim they appear in (as reporter, as witness, or
//     as the assigned adjuster's own contact data).
//
//   * personal-data anonymise (APPI right to erasure):
//     redact the PII fields on a single claim while preserving
//     the full AuditEvent trail. Notes and reserves remain, but
//     PII columns are nulled and `_ct` blobs are zeroised.
//
// PII inventory (brief.md §PII inventory):
//
//   Standard PII (cleartext, masked at API):
//     reporter_name, reporter_relation_to_insured,
//     loss_location_{prefecture,postal_code,detail}.
//
//   Special-care PII (AES-256-GCM at rest, never returned in
//   normal API responses — only via this module):
//     reporter_phone_ct, reporter_email_ct,
//     insured_government_id_ct, bank_account_for_payout_ct,
//     injury_details_ct, witness_phone_ct.
//
// The service decrypts all `_ct` blobs only when assembling a
// data-subject export — the encryption envelope (see
// `common/encryption.ts`) is the only path that touches the
// KEK. The anonymise path overwrites the columns with NULL and
// emits a single audit event recording the erasure metadata.
// ─────────────────────────────────────────────────────────────────────────

import {
  BadRequestException,
  ForbiddenException,
  Injectable,
  Logger,
  NotFoundException,
} from '@nestjs/common';
import { UserRole } from '@prisma/client';

import { decryptPii } from '../common/encryption';
import { PrismaService } from '../prisma.service';
import { AnonymiseRequestDto } from './dto/anonymise-request.dto';

/**
 * Authenticated-caller envelope. Mirrors the shape used by the
 * reserves and claims services so the controller layer can pass
 * a single projection through every guard-gated entry point.
 */
export interface AppiCaller {
  id: string;
  role: UserRole;
}

/**
 * Per-claim PII projection included in a data-subject export.
 * Every field that could conceivably hold personal information
 * about the subject is enumerated explicitly — the export is a
 * disclosure document, so being exhaustive matters more than
 * being compact.
 */
export interface DataSubjectClaimRecord {
  claim_id: string;
  appearance: 'reporter' | 'witness';
  policy_number: string;
  loss_date: Date;
  incident_type: string;
  status: string;
  appi_consent_version: string;
  appi_consent_at: Date;
  reporter_name: string | null;
  reporter_relation_to_insured: string | null;
  reporter_phone: string | null;
  reporter_email: string | null;
  insured_government_id: string | null;
  bank_account_for_payout: string | null;
  injury_details: string | null;
  loss_location: {
    prefecture: string;
    postal_code: string;
    detail: string;
  } | null;
  witness_statements: Array<{
    id: string;
    witness_name: string;
    witness_phone: string | null;
    statement_body: string;
    inkan_seal_hash: string;
    recorded_at: Date;
  }>;
}

/**
 * Top-level shape returned by `dataSubjectExport`. The
 * envelope carries provenance metadata alongside the records
 * so the data subject (and the regulator) can verify the
 * export was generated by this system at this time.
 */
export interface DataSubjectExportPayload {
  generated_at: string;
  data_subject_name: string;
  requested_by_actor_id: string;
  requested_by_role: UserRole;
  appi_article: 'Article 28';
  record_count: number;
  records: DataSubjectClaimRecord[];
}

/**
 * Shape returned by `anonymise`. Mirrors the audit-row payload
 * so callers (tests, downstream integrations) can confirm what
 * was erased without re-reading the database.
 */
export interface AnonymiseResultPayload {
  claim_id: string;
  anonymised_at: string;
  appi_request_reference: string;
  fields_redacted: string[];
  audit_trail_preserved: true;
}

@Injectable()
export class AppiService {
  private readonly logger = new Logger(AppiService.name);

  constructor(private readonly prisma: PrismaService) {}

  // ─────────────────────────────────────────────────────────────
  // Data-subject export — APPI Article 28
  // ─────────────────────────────────────────────────────────────

  /**
   * Assemble a single document containing every piece of PII
   * the platform holds about the named individual.
   *
   * Resolution strategy:
   *   1. Find every Claim where `reporter_name` matches exactly.
   *      The platform treats `reporter_name` as the canonical
   *      reporter identifier (we don't synthesise a separate
   *      "data subject" entity in Track A).
   *   2. Find every WitnessStatement where `witness_name`
   *      matches exactly — the same individual may appear as a
   *      witness on a different claim.
   *   3. Decrypt every `_ct` blob inline so the export is
   *      self-contained.
   *
   * The caller's role is gated upstream (auditor or manager —
   * see the controller's `@Roles` decorator and the role matrix
   * in brief.md). We re-check here as defence in depth.
   */
  async dataSubjectExport(
    data_subject_name: string,
    caller: AppiCaller,
  ): Promise<DataSubjectExportPayload> {
    if (caller.role !== 'auditor' && caller.role !== 'manager') {
      throw new ForbiddenException(
        'Data-subject export is restricted to auditors and managers.',
      );
    }

    const trimmed = data_subject_name.trim();
    if (trimmed.length === 0) {
      throw new BadRequestException(
        'data_subject_name is required and must be non-empty.',
      );
    }

    // Reporter-side claims — the individual is the FNOL reporter.
    const reporter_claims = await this.prisma.claim.findMany({
      where: { reporter_name: trimmed },
      include: { witness_statements: true },
      orderBy: { created_at: 'asc' },
    });

    // Witness-side claims — the individual gave a statement on
    // someone else's claim. We exclude claims already pulled in
    // via the reporter path to keep the export deduplicated.
    const reporter_claim_ids = new Set(reporter_claims.map((c) => c.id));
    const witness_rows = await this.prisma.witnessStatement.findMany({
      where: { witness_name: trimmed },
      include: { claim: true },
      orderBy: { recorded_at: 'asc' },
    });

    const witness_only_claims = new Map<
      string,
      (typeof witness_rows)[number]['claim'] & {
        witness_statements: (typeof witness_rows)[number][];
      }
    >();
    for (const row of witness_rows) {
      if (reporter_claim_ids.has(row.claim_id)) continue;
      const existing = witness_only_claims.get(row.claim_id);
      if (existing) {
        existing.witness_statements.push(row);
      } else {
        witness_only_claims.set(row.claim_id, {
          ...row.claim,
          witness_statements: [row],
        });
      }
    }

    const records: DataSubjectClaimRecord[] = [];

    for (const claim of reporter_claims) {
      records.push({
        claim_id: claim.id,
        appearance: 'reporter',
        policy_number: claim.policy_number,
        loss_date: claim.loss_date,
        incident_type: claim.incident_type,
        status: claim.status,
        appi_consent_version: claim.appi_consent_version,
        appi_consent_at: claim.appi_consent_at,
        reporter_name: claim.reporter_name,
        reporter_relation_to_insured: claim.reporter_relation_to_insured,
        reporter_phone: this.tryDecrypt(claim.reporter_phone_ct, 'reporter_phone'),
        reporter_email: this.tryDecrypt(claim.reporter_email_ct, 'reporter_email'),
        insured_government_id: this.tryDecrypt(
          claim.insured_government_id_ct,
          'insured_government_id',
        ),
        bank_account_for_payout: this.tryDecrypt(
          claim.bank_account_for_payout_ct,
          'bank_account_for_payout',
        ),
        injury_details: this.tryDecrypt(claim.injury_details_ct, 'injury_details'),
        loss_location: {
          prefecture: claim.loss_location_prefecture,
          postal_code: claim.loss_location_postal_code,
          detail: claim.loss_location_detail,
        },
        witness_statements: claim.witness_statements
          .filter((w) => w.witness_name === trimmed)
          .map((w) => ({
            id: w.id,
            witness_name: w.witness_name,
            witness_phone: this.tryDecrypt(w.witness_phone_ct, 'witness_phone'),
            statement_body: w.statement_body,
            inkan_seal_hash: w.inkan_seal_hash,
            recorded_at: w.recorded_at,
          })),
      });
    }

    for (const claim of witness_only_claims.values()) {
      records.push({
        claim_id: claim.id,
        appearance: 'witness',
        policy_number: claim.policy_number,
        loss_date: claim.loss_date,
        incident_type: claim.incident_type,
        status: claim.status,
        appi_consent_version: claim.appi_consent_version,
        appi_consent_at: claim.appi_consent_at,
        // The subject is *not* the reporter on this claim, so we
        // omit the reporter-side PII columns — disclosing them
        // here would leak a third party's data.
        reporter_name: null,
        reporter_relation_to_insured: null,
        reporter_phone: null,
        reporter_email: null,
        insured_government_id: null,
        bank_account_for_payout: null,
        injury_details: null,
        loss_location: null,
        witness_statements: claim.witness_statements.map((w) => ({
          id: w.id,
          witness_name: w.witness_name,
          witness_phone: this.tryDecrypt(w.witness_phone_ct, 'witness_phone'),
          statement_body: w.statement_body,
          inkan_seal_hash: w.inkan_seal_hash,
          recorded_at: w.recorded_at,
        })),
      });
    }

    const payload: DataSubjectExportPayload = {
      generated_at: new Date().toISOString(),
      data_subject_name: trimmed,
      requested_by_actor_id: caller.id,
      requested_by_role: caller.role,
      appi_article: 'Article 28',
      record_count: records.length,
      records,
    };

    this.logger.log(
      `APPI Article 28 export assembled: subject="${trimmed}" records=${records.length} actor=${caller.id}`,
    );

    return payload;
  }

  // ─────────────────────────────────────────────────────────────
  // Anonymise — APPI right to erasure (audit trail preserved)
  // ─────────────────────────────────────────────────────────────

  /**
   * Redact the PII on a single claim while preserving the
   * AuditEvent trail. The operation is irreversible: encrypted
   * blobs are nulled (the per-record DEK is lost with them),
   * cleartext PII columns are replaced with redaction markers,
   * and a final audit event is written recording the metadata.
   *
   * Notes, evidence, witness statements, and reserves are kept
   * structurally but their PII columns are redacted in place.
   * The downstream regulator can still walk the claim's
   * lifecycle (FNOL → assignment → reserves → close) without
   * being able to identify the individuals involved.
   *
   * Per the role matrix in brief.md (Track A), no role is
   * authorised for this operation — the route exists for
   * surface stability but the service rejects every caller.
   * Track B will open it to managers; the gate is encoded here
   * so tests can pin the current behaviour.
   */
  async anonymise(
    claim_id: string,
    dto: AnonymiseRequestDto,
    caller: AppiCaller,
  ): Promise<AnonymiseResultPayload> {
    // Track A: no role is authorised. Reject every caller.
    // Track B opens this to manager; flipping the gate is a
    // single-line edit with its own test.
    throw new ForbiddenException(
      'PII anonymisation is not enabled in Track A. See brief.md role matrix; Track B opens this to managers.',
    );

    // The remainder of this method is dead code in Track A but
    // documents the intended Track B implementation so the
    // surface is reviewable. It is kept behind the throw above
    // so it cannot accidentally execute.
    //
    // eslint-disable-next-line @typescript-eslint/no-unreachable
    void this.performAnonymise(claim_id, dto, caller);
  }

  /**
   * Track-B-ready anonymise implementation. Not reachable in
   * Track A — see `anonymise` above. Kept as a separate method
   * so the dead-code block doesn't pollute the public API.
   */
  private async performAnonymise(
    claim_id: string,
    dto: AnonymiseRequestDto,
    caller: AppiCaller,
  ): Promise<AnonymiseResultPayload> {
    if (dto.acknowledge_irreversible !== true) {
      throw new BadRequestException(
        'acknowledge_irreversible must be true to proceed with anonymisation.',
      );
    }

    const claim = await this.prisma.claim.findUnique({
      where: { id: claim_id },
    });
    if (!claim) {
      throw new NotFoundException(`Claim ${claim_id} not found.`);
    }

    if (claim.reporter_name !== dto.data_subject_name.trim()) {
      throw new BadRequestException(
        'data_subject_name does not match the claim reporter; refusing to anonymise.',
      );
    }

    const redaction_marker = '[REDACTED-APPI]';
    const fields_redacted = [
      'reporter_name',
      'reporter_phone_ct',
      'reporter_email_ct',
      'reporter_relation_to_insured',
      'insured_government_id_ct',
      'bank_account_for_payout_ct',
      'injury_details_ct',
      'loss_location_detail',
      'initial_description',
      'police_report_number',
    ];

    await this.prisma.$transaction(async (tx) => {
      await tx.claim.update({
        where: { id: claim_id },
        data: {
          reporter_name: redaction_marker,
          reporter_phone_ct: null,
          reporter_email_ct: null,
          reporter_relation_to_insured: redaction_marker,
          insured_government_id_ct: null,
          bank_account_for_payout_ct: null,
          injury_details_ct: null,
          loss_location_detail: redaction_marker,
          initial_description: redaction_marker,
          police_report_number: null,
        },
      });

      // Witness statements on this claim — redact the witness
      // PII but keep the inkan_seal_hash and statement skeleton
      // for audit reconstruction.
      await tx.witnessStatement.updateMany({
        where: { claim_id },
        data: {
          witness_name: redaction_marker,
          witness_phone_ct: null,
          statement_body: redaction_marker,
        },
      });

      // Final audit event recording the erasure metadata. The
      // app-level audit interceptor would normally write this,
      // but anonymise is destructive and we want the audit row
      // to land in the same transaction as the redaction.
      await tx.auditEvent.create({
        data: {
          actor_id: caller.id,
          actor_role: caller.role,
          action: 'appi.anonymised',
          claim_id,
          target_id: claim_id,
          payload_hash: this.hashPayload({
            claim_id,
            reason: dto.reason,
            appi_request_reference: dto.appi_request_reference,
            fields_redacted,
          }),
          request_id: 'appi-anonymise',
          correlation_id: dto.appi_request_reference,
        },
      });
    });

    this.logger.warn(
      `APPI anonymisation executed: claim=${claim_id} ref=${dto.appi_request_reference} actor=${caller.id}`,
    );

    return {
      claim_id,
      anonymised_at: new Date().toISOString(),
      appi_request_reference: dto.appi_request_reference,
      fields_redacted,
      audit_trail_preserved: true,
    };
  }

  // ─────────────────────────────────────────────────────────────
  // Helpers
  // ─────────────────────────────────────────────────────────────

  /**
   * Decrypt a `_ct` blob, returning `null` on either a null
   * input or a decryption failure. The export endpoint is the
   * *only* place we surface special-care PII, so failures here
   * are logged loudly but do not abort the entire export — a
   * partial disclosure is preferable to an opaque 500 when the
   * subject has the legal right to whatever we can produce.
   */
  private tryDecrypt(ct: Buffer | Uint8Array | null, field: string): string | null {
    if (!ct) return null;
    try {
      const buf = Buffer.isBuffer(ct) ? ct : Buffer.from(ct);
      return decryptPii(buf);
    } catch (err) {
      this.logger.error(
        `APPI export: failed to decrypt field=${field} err=${(err as Error).message}`,
      );
      return null;
    }
  }

  /**
   * Stable SHA-256 hash of a JSON-canonicalised payload, used
   * as the `payload_hash` value on the anonymise audit event.
   * Mirrors the hashing convention used by
   * `common/audit.interceptor.ts`.
   */
  private hashPayload(payload: unknown): string {
    // eslint-disable-next-line @typescript-eslint/no-var-requires
    const { createHash } = require('crypto') as typeof import('crypto');
    const canonical = JSON.stringify(payload, Object.keys(payload as object).sort());
    return createHash('sha256').update(canonical).digest('hex');
  }
}